#include<stdio.h>
int main()
{
    int a,b,sum;                //声明a,b,sum
    a=123;                      //定义a赋值为123
    b=456;                      //定义b赋值为456
    sum=a+b;                    //sum取和
    printf("sum is %d\n",sum);  //输出格式化的sum   %d 为整数型格式
    return 0;                   //返回0值
}   
